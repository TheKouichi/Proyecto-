package herenciaypolimorfismo;
/**
 * Figura plana es una clase abstracta. Una clase abstracta es aquella que por lo menos tiene un método abstracto.
 * Un método abstracto es una herramienta que tiene Java para implementar polimorfismo.
 * No se pueden crear objetos en una clase abstracta.
 * @author Iván
 *
 */
public abstract class FiguraPlana 
{
	//Atributos
	protected double base;
	protected double altura;
	
	public FiguraPlana(double base, double altura)
	{
		this.base = base;
		this.altura = altura;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double getBase() {
		return base;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setBase(double base) {
		this.base = base;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double getAltura() {
		return altura;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setAltura(double altura) {
		this.altura = altura;
	}
	///////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String mostrarDatos()
	{
		return "\n" + base + "\n" + altura;
	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Método abstracto
	public abstract double area();
}
